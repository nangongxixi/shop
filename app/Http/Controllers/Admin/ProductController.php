     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        //所有的可用分类
        $categories = Category::orderByRaw('concat(path, id)')
            ->where('status','=',Category::STATUS_YES)
            ->get();
        $product = new Product();
        return view('admin.product.create')->with([
            'categories'=>$categories,
            'product'=>$product,
        ]);
    }

